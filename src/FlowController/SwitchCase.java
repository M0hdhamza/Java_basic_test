package FlowController;

public class SwitchCase {

	public static void main(String[] args) {
		int a=2;
		switch(a) {
		case 1:
			System.out.println("This is case 1");
			break;
		case 2:
			System.out.println("This is case 2");
			break;
		default:
			System.out.println("There is no a value");
		}
		
	}
}
