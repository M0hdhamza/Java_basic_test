package Operators;

public class UnaryOperators {

	public static void main(String[] args) {
		//PreIncrement//
		int x=10;
		int y=++x;
		System.out.println(y);
		System.out.println(x);
		
		//PostIncrement//
		int a=11;
		int b=a++;
		System.out.println(b);
		System.out.println(a);
		
		//PreDecressment//
		int c=10;
		int d=--c;
		System.out.println(c);
		
		//PostDecrement//
		int e=10;
		int f=e--;
		System.out.println(f);
	}
	

}
